constexpr int N = 1e6 + 6;

void solve()
{
    int x, y;
    cin >> x >> y;
    int ans = 0;
    while (x < 3)
    {
        if (x == 2 || y == 2)
        {
            ans += 2;
        }
        else
        {
            ans++;
        }
        x++;
    }

    cout << ans << '\n';
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    int tt;
    tt = 1;
    while (tt--)
    {
        solve();
    }

#ifdef LOCAL
    cerr << "Time elapsed: " << clock() / 1000 << " ms" << endl;
#endif
    return 0;
}
