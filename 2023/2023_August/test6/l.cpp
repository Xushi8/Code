const int N = 1000005;

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(0);

    int n;
    cin >> n;
    vector<int> arr(n);
    for (int i = 0; i < n; i++)
    {
        cin >> arr[i];
    }

    ull ans = 0;
    ull pairs = 0;
    for (int i = n - 1; i >= 0; i--)
    {
        pairs += arr[i] / 2;
        if (arr[i] & 1 && pairs > 0)
        {
            ans++;
            pairs--;
        }
    }

    ans += 2 * pairs / 3;
    cout << ans << '\n';

#ifdef LOCAL
    cerr << "Time elapsed: " << clock() / 1000 << " ms" << endl;
#endif
    cout << endl;
    return 0;
}